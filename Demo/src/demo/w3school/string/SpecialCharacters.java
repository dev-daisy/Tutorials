package demo.w3school.string;

public class SpecialCharacters {
	public static void main(String args[]){

		// The sequence \"  inserts a double quote in a string:
		String txt = "We are the so-called \"Vikings\" from the north.";
		System.out.println(txt);

		// The sequence \'  inserts a single quote in a string:
		txt = "It\'s alright.";
		System.out.println(txt);

		// The sequence \\  inserts a single backslash in a string:
		txt = "The character \\ is called backslash.";
		System.out.println(txt);

		// The \n New Line
		txt = "The character \n is called New Line.";
		System.out.println(txt);
		System.out.println(txt);

		// The \r Carriage Return
		txt = "The character \r is called Carriage Return.";
		System.out.println(txt);

		// The \t Tab
		txt = "The character \t is called Tab.";
		System.out.println(txt);

		// The \b Backspace
		txt = "The character \b is called Backspace.";
		System.out.println(txt);


		// The \f Form Feed
		txt = "The character \f is called Form Feed.";
		System.out.println(txt);

	}
}
